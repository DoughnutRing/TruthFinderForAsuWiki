    def __get_proxies(self):
        with open('ippool.txt', 'r', encoding='UTF-8') as f:
            for line in f:
                self.proxypool.append({'HTTP': line[:-1]})
        print(f"Proxy pool loading complete with {len(self.proxypool)} IPs.")

    def __get_proxy(self):
        return random.choice(self.proxypool)

    def __get_header(self):
        return {
            'User-Agent': random.choice(self.USER_AGENTS),
            'Accept': 'text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8',
            'Accept-Language': 'en-US,en;q=0.5',
            'Connection': 'keep-alive',
            'Accept-Encoding': 'gzip, deflate',
        }

    def connect_truth(self, url:str, need_txt = False, num_truth = 0):
        '实现真相内容的获取'
        header = self.__get_header()
        prox = self.__get_proxy()

        html_txt = ''
        try:
            response = self.scraper.get(url,headers=header,proxies=prox)
            if response.status_code == 200 and len(response.text):
                self.count += 1
                # flow_txt = __get_flow(response.content)
                # print(str(count) + ": from " + prox['HTTP'] + " to " + URL + " successfully. Used " + flow_txt + ".")
                if self.count < 1000 and self.count%10==0 or self.count % 100 == 0:
                    print(f"IP地址位于{prox['HTTP']}的纯路人已发现真相，总计{self.count}人选择保平安。真相之书已书写{num_truth}页。\n", end='')
                if need_txt:
                    html_txt = response.text
                self.scraper.cookies.clear()
            else:
                # print(str(count) + ": from " + prox['HTTP'] + " to " + URL + " failed.")
                print(f"IP地址位于{prox['HTTP']}的纯路人发现真相失败！\n", end='')
                # print(response)
            response.close()
        except Exception as ex:
            print(ex)
            print(f"IP地址位于{prox['HTTP']}的纯路人发现真相失败！\n", end='')
        
        return html_txt

